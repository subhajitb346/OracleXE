CREATE TABLE SAILOR_00028(
   SID VARCHAR2(4) PRIMARY KEY CHECK (SID LIKE 's%'),
   SNAME VARCHAR2(10) CHECK (SNAME= INITCAP(SNAME)),
   MNAME VARCHAR2(10),
   SURNAME VARCHAR2(10) NOT NULL,
   RATING NUMBER(2) DEFAULT 0,
   AGE NUMBER(3,1) NOT NULL
);

